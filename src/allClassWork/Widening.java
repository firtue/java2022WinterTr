public class Widening {
    public static void main(String[] args) {

        byte s1=16;
        short s2=s1;
        int s3=s2;
        long s4=s3;
        float s5=s4;
        double s6=s5;

        System.out.println("byte="+s1+"    short="+s2+"    int="+s3+"    long="+s4+"    float="+s5+"     double="+s6);
    }
}
